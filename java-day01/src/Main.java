public class Main {
    public static void main(String[] args) { // 메소드
        // 명령어가 들어가는 공간
        // 변수 선언 = 데이터 타입 + 식별자
        //정수형
        int iv; // 변수 선언 JS 에서 var iv 또는 let iv 와 같은 변수 선언
        // 문자형 변수
        char cv;
        // long 형 변수의 초기화
        long lv = 100000L; // long 타입은 값의 끝에 'L'을 붙임
        // float 형 변수의 초기화
        float pi = 3.14f; // float 타입은 값의 끝에 'f'를 붙임
        // long, float 타입만 값의 끝에 변수 첫 글자를 마지막에 붙임
        // double 형 변수 초기화
        double dv = 9.888;
        //boolean
        boolean bv = true; // 또는 false
        // 문자열 변수
        String str = "abc"; // 첫 글자는 대문자로 시작함
    }
}
